#!/usr/bin/env bash

# Set some of the colors mentioned by the sexy bash prompt
# https://github.com/twolfson/sexy-bash-prompt#configurationi
# Color Guide: https://linuxtidbits.files.wordpress.com/2008/08/tputcolors.png?w=776
PROMPT_USER_COLOR="$(tput bold)$(tput setaf 9)"
PROMPT_PREPOSITION_COLOR="$(tput bold)$(tput setaf 7)"
